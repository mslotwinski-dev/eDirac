    <h3>Biomateriały i nanotechnologia</h3>
    <Section>
      Żyjemy w społeczeństwie przemysłowym, które jako cel postawiło sobie
      maksymalizację zysków nawet kosztem zniszczenia naszej planety. Ziemia
      jest zanieczyszczana i eksploatowana na każdy możliwy sposób. Nikt nie
      myśli o tym jaki świat zostawimy kolejnym pokoleniom. Niestety temat ten
      został przejęty przez polityków, którzy jak wiadomo są mistrzami w sztuce
      rozwiązywania problemów których nie ma oraz pogłębiania problemów, które
      są. Ludzie lubią proste rozwiązania, ale nie uratuje nas biurokracja,
      wyższy podatek czy doniosłe przemówienie. Potrzebujemy stworzyć
      technologię, która wolna będzie od produkcji zanieczyszczeń. Właśnie tutaj
      (jako jedna z wielu dziedzin) wkracza inżynieria materiałowa.
    </Section>
    <Section>
      Materiały biodegradowalne to grupa substancji, które mają zdolność do
      naturalnego rozkładu w środowisku pod wpływem działania mikroorganizmów,
      takich jak bakterie, grzyby czy enzymy. Przez lata na Ziemii nagromadziła
      się taka ilość odpadów, że powoli wymyka się nam to spod kontroli.
      Odpowiedzią na to są badania nad ograniczeniem tego zjawiska, które są
      prowadzone w dwóch kierunkach. Po pierwsze biotechnologia pracuje nad
      pozyskiwaniem oraz zwiększeniem możliwości mikroorganizmów tak, aby
      potrafiły rozkładać plastik.
    </Section>
