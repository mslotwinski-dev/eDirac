      tworząc nowe materiały zawierające elementy wrażliwe na działanie
      mikroorganizmów.
    </Section>
    <Section>
      Badania nad materiałami pod kątem ich biologicznych właściwości
      doprowadziły do ogromnego przełomu nie tylko w ekologii, ale co
      najważniejsze, w medycynie. Substancje te nazywamy biomateriałami, a
      projektowane są do zadań, które wymagają bezpośredniego kontaktu z
      tkankami organizmu. Biomateriały znajdują szerokie zastosowanie w
      medycynie, od implantów, protez i stentów po materiały wspomagające
      gojenie się ran i naprawę tkanek. Materiały te muszą być zatem zgodne z
      tkankami organizmu, aby nie wywoływać reakcji zapalnych ani toksycznych.
      Biokompatybilność jest kluczowa, ponieważ zapewnia, że biomateriał nie
      wywoła odrzutu lub szkodliwych reakcji immunologicznych. Inną cechą jaką
      należy im zapewnić jest biostabilność lub biodegradowalność. Niektóre
      biomateriały, takie jak endoprotezy, powinny być trwałe i biostabilne, aby
      zachowały swoje właściwości przez długi czas. Inne, jak materiały
      stosowane do czasowego wypełnienia ubytków w tkankach, mogą być
      biodegradowalne, dzięki czemu po określonym czasie ulegają rozkładowi i są
      wchłaniane przez organizm.
    </Section>
    <Section>
      W przyszłości biomateriały mogą wspierać rozwój technologii medycznych,
      takich jak druki 3D tkanek i implantów na zamówienie, czy materiały
      inteligentne, które dostosowują się do zmieniających się warunków
      organizmu. Trwają też badania nad biomateriałami antybakteryjnymi, które
      mogą zapobiegać infekcjom, oraz nad materiałami zdolnymi do autoodnowy, co
      pozwoli na dłuższą trwałość implantów. Przykładem naturalnej substancji
      antybakteryjnej jest złoto.
    </Section>
    <Section>
      Inny kierunek rozwoju w fizyce materiałów rozwinął się w oparciu o badania
      nad nanomateriałami. Nanotechnologia to dziedzina nauki zajmująca się
      manipulacją materią na poziomie nanometrów. Jest to skala na poziomie
      pojedynczych atomów i molekuł, co umożliwia tworzenie materiałów i
      urządzeń o unikalnych właściwościach fizycznych, chemicznych i
      biologicznych. Na tej skali pojawiają się nowe zjawiska, które pozwalają
      na tworzenie funkcji i właściwości niedostępnych w materiałach
      makroskopowych. Wśród nich znajdziemy takie układy jak nanorurki węglowe
      cechujące się wyjątkową wytrzymałością mechaniczną oraz wysoką
      przewodnością, a także grafen, który po tym jak zyskał sławę w każdym
      zakątku świata, usunął się w cień.
    </Section>
    <Section>
      W takich materiałach znajdziemy struktury unikalne dla tej skali. Możemy
      na przykład stworzyć nanowarstwę, która będzie pełnić rolę ochronną albo
      wpływać na właściwości optyczne. Znacznie ciekawszymi strukturami są
      kropki kwantowe, których opis wymaga zaawansowanej mechaniki kwantowej.
      Warto jednak wiedzieć, że od lat znajdują coraz więcej zastosowań,
      szczególnie w medycynie, gdzie są stosowane na przykład jako znaczniki
      (znacznie bardziej precyzyjne niż zwykłe barwniki). Są one również
      wykorzystywane w fotowoltaice. Cały rozwój nanotechnologii wspomaga
      elektronikę pozwalając na jej miniaturyzację oraz poprawiają wydajność
      magazynowania energii (baterie). Brzmi to tak odlegle, że nie zdajemy
      sobie sprawy, że nanotechnologia jest stosowana nawet w naszych ubraniach,
      gdzie używa się nanocząstek srebra, które działają antybakteryjnie.
