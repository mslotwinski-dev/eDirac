      sód przekazuje chlorowi jeden elektron, przez co tworzą się dwa jony -
      <M s="Na^+" /> oraz <M s="Cl^-" />. Teraz nasze atomy są szczęśliwe, ale
      przez to że ilość ich elektronów się zmieniła przestały być elektrycznie
      obojętne. A to oznacza, że zgodnie z prawem Coulomba (2.33) powinny się
      przyciągać. Powstaje wtedy trwałe wiązanie między nogami, a powstały w ten
      sposób dwuatomowy związek to NaCl (chlorek sodu), który znasz jako sól
      kuchenna. Energia wiązania między jonami chlorku sodu wynosi
